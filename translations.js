let currentLang = 'ru';

function setLang(lang) {
  currentLang = lang;
  document.documentElement.lang = lang;
  renderPage();
  updateActiveLangBtn();
}

function updateActiveLangBtn() {
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === currentLang);
  });
}

function t(key) {
  const keys = key.split('.');
  let obj = translations[currentLang];
  for (const k of keys) {
    if (obj && obj[k] !== undefined) obj = obj[k];
    else return key;
  }
  return obj;
}

function renderPage() {
  document.getElementById('nav-logo').textContent = t('nav.logo');
  document.getElementById('nav-subtitle').textContent = t('nav.subtitle');
  document.getElementById('nav-about').textContent = t('nav.about');
  document.getElementById('nav-languages').textContent = t('nav.languages');
  document.getElementById('hero-greeting').textContent = t('hero.greeting');
  document.getElementById('hero-subtitle').textContent = t('hero.subtitle');
  document.getElementById('hero-desc').textContent = t('hero.description');
  document.getElementById('hero-experience').textContent = t('hero.experience');
  document.getElementById('hero-price').textContent = t('hero.price');
  document.getElementById('hero-btn').textContent = t('hero.btnContact');
  document.getElementById('about-title').textContent = t('about.title');
  document.getElementById('about-text').textContent = t('about.text');
  document.getElementById('lang-section-title').textContent = t('languages.title');

  const langItems = t('languages.items');
  document.querySelectorAll('.lang-card').forEach((card, i) => {
    if (langItems[i]) {
      card.querySelector('.lang-name').textContent = langItems[i].name;
      card.querySelector('.lang-desc').textContent = langItems[i].desc;
      card.querySelector('.lang-level').textContent = langItems[i].level;
    }
  });

  const reviewItems = t('reviews.items');
  const marqueeContent = document.getElementById('marquee-content');
  marqueeContent.innerHTML = '';
  const allReviews = [...reviewItems, ...reviewItems];
  allReviews.forEach(text => {
    const span = document.createElement('span');
    span.textContent = text;
    marqueeContent.appendChild(span);
  });

  document.getElementById('footer-title').textContent = t('footer.title');
  document.getElementById('footer-subtitle').textContent = t('footer.subtitle');
  document.getElementById('footer-quote').textContent = t('footer.quote');
  document.getElementById('footer-tg').textContent = t('footer.tg');
  document.getElementById('footer-wa').textContent = t('footer.wa');
  document.getElementById('footer-kakao').textContent = t('footer.kakao');
  document.getElementById('footer-email').textContent = t('footer.email');
}